# no tiene valor
nada = None
print(nada)
print(type(nada))

# cadena
cadena = "Hola soy texto"
print(cadena)
print(type(cadena))

# entero 
entero = 88
print(entero)
print(type(entero))

# decimal
flotante = 4.3
print(flotante)
print(type(flotante))

# booleano 
booleano =  True
print(booleano)
print(type(booleano))

# tipo de lista si cambia
lista = [10, 20 , 30, 40]
print(lista)
print(type(lista))

# tuplas no cambia 
tuplas = ("Master", "en", "python")
print(tuplas)
print(type(tuplas))

#diccionarios 
diccionario ={
    "nombre": "Gerardo",
    "apellido":"garduno",
    "curso": "Master en python"
}
print(diccionario)
print(type(diccionario))

# funcion para rango 
rango  = range(9)
print(rango)
print(type(rango))

# tipos de dato byte
dbyte = b"Probando"
print(dbyte)
print(type(dbyte))