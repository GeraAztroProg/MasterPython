#Operadores Aritmeticos
numero1= 77
numero2 = 44 # operadorador de asignacion =

resta = numero1 - numero2
suma = numero1 + numero2
mul = numero1 * numero1
div = numero1 / numero2
resD = numero1 % numero2
divE = numero1 // numero2
pot = numero1 ** 2

print("***************CALCULADORA****************")
print(f"**     La resta es {resta}                   **")
print(f"**     La suma es {suma}                   **")
print(f"**     La multiplicacion es {mul}        **")
print(f"**     La division es {div}              **")
print(f"**     El resto de division es {resD}       **")
print(f"**     La division exacta es {divE}          **")
print(f"**     La potencia es {pot}              **")
print("******************************************")